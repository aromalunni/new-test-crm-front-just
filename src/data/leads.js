// ---- Helpers ----

export function getLeadById(id) {
  return leads.find((lead) => lead.id === id) || null
}

export function getLeadsByOwner(repId) {
  return leads.filter((lead) => lead.ownerRepId === repId)
}

export function getLeadsByStage(stage) {
  return leads.filter((lead) => lead.stage === stage)
}

export function getLeadsByDistrict(district) {
  return leads.filter((lead) => lead.district === district)
}

// Count of leads in each stage, in canonical stage order.
export function getStageCounts(leadList = leads) {
  return STAGES.map((stage) => ({
    stage,
    count: leadList.filter((lead) => lead.stage === stage).length,
  }))
}

